package com.country.bean;

public class Country {
	
	public String countryName;
	public String totalPopulation;
	public String totalMalePopulation;
	public String getTotalPopulation() {
		return totalPopulation;
	}
	public void setTotalPopulation(String totalPopulation) {
		this.totalPopulation = totalPopulation;
	}
	public String getCountryName() {
		return countryName;
	}
	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}
	public String getTotalMalePopulation() {
		return totalMalePopulation;
	}
	public void setTotalMalePopulation(String totalMalePopulation) {
		this.totalMalePopulation = totalMalePopulation;
	}
	public String getTotalFemalePopulation() {
		return totalFemalePopulation;
	}
	public void setTotalFemalePopulation(String totalFemalePopulation) {
		this.totalFemalePopulation = totalFemalePopulation;
	}
	public String totalFemalePopulation;
	

}
